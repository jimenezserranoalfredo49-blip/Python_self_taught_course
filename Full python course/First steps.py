#variable = like math it's a container of an assigned value that can be: Integer, string, float, boolean

#Strings = series of characters. Even if you use a number it will be treated as a string

first_name = 'Bro'
print(f"Hello {first_name}")
#f strings like the one above allow me to put expressions inside string literals minimizing the syntax

#Integers = whole numbers, don't use quotes, those are for integers

age = 18
print(f"You are {age} years old")

# Float = numbers with a decimal part

price = 10.99
print(f"The price is ${price}")

#Boolean = True of False statements, not used in an isolated form but within "if" statements

is_student = True
print(f"Are you a student?: {is_student}")